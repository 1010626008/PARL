import random
import numpy as np
import torch


def set_seed(seed):
    """
    Set the seed for all the possible random number generators
    for global packages.

    Usage: parl.set_seed(1024)

    """
    seed = int(seed)
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)

    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)
